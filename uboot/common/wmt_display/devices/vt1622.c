{
#ifdef CONFIG_KERNEL
  i2c_api_register_write(VT1622_ADDR, 1, addr, data);
  return 0;
#else
  return i2c1_write(VT1622_ADDR, addr, 1, &data, 1);
#endif
}


void vt1622_dumpRegs(void)
{
  int i = 0;
  int j = 0;
  int count;
  uint addr0 = 0x0000;

  DBG_DETAIL("START.\n");		
  addr0 = 0x0000 ;
  count = 0;
  for(i = 0 ; i < 16 && count < encoder_table_size; i++)
  {
	  printk("vt1622: %04x:", addr0);
	  for (j = 0; j < 16 && count < encoder_table_size; j++) {
		  printk(" %02x", vt1622_i2c_read(addr0));
		  addr0++;
      count++;
	  }
	  printk("\n");
  }
  DBG_DETAIL("END.\n");
}

int vt1622_isEnable(void)
{
  //vt1622_dumpRegs();
  return vt1622_i2c_read(0x1B) == 0x10;
}

void vt1622_reinitialize_table(void)
{
  int	count = 0 ;
  ulong	addr0 = 0x0000 ;

  char* table0 = vt1622_getTable0();

  DBG_DETAIL("Start\n");

  DBG_DETAIL("vt1622: vt1622_i2c_write START.\n");

  for(count = 0; count < encoder_table_size; count++)
  {
	  vt1622_i2c_write(addr0++, table0[count]);
  }

  DBG_DETAIL("vt1622: vt1622_i2c_write END.\n");

  vt1622_dumpRegs();

  DBG_DETAIL("End\n");
}

int vt1622_init(struct vout_s *vo)
{
	vt1622_not_ready = 1;


  if(vt1622_isEnable())
  {
    vt1622_reinitialize_table();

	  vt1622_not_ready = 0;

	  DPRINT("[VT1622] DVI ext device\n");
	
    return 0;
  }
  else
  {
    DPRINT("[VT1622] not exist\n");
    return -1;
  }
}

