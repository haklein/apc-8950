{
#ifdef CONFIG_KERNEL
  return i2c_api_register_read(VT1622_ADDR, 1, addr);
#else
  uchar data = 0;
  i2c1_read(VT1622_ADDR, addr, 1, &data, 1);
  return data;
#endif
}

